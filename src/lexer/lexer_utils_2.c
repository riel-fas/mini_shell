#include "lexer.h"

int	is_whitespace(char a)
{
	return (a == ' ' || a == '\t' || a == '\n' || a == '\r');
}

int	is_operator(char a)
{
	return (a == '|' || a == '<' || a == '>');
}

int	is_quote(char a)
{
	return (a == '\'' || a == '\"');
}
